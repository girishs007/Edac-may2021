class Q15
{
	public static void main(String args[])
	{
		int s1=10;
		int s2=20;
		int temp;
		System.out.println("s1 :"+s1);
		System.out.println("s2 :"+s2);
		
		temp=s1;
		s1=s2;
		s2=temp;
		System.out.println("After swapping");
		System.out.println("s1 :"+s1);
		System.out.println("s2 :"+s2);
		
	}
}